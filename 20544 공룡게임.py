# 공룡게임
# Gold 3, DP

M = 10**9+7

n = int(input())

# [바닥번호, 2의 유무, 연속인가, 높이]
dp = [[[[0,0,0] for _ in range(2)] for _ in range(2)] for _ in range(n)]
dp[0][0][0][0] = 1

for i in range(1,n):
    # 높이 1
    dp[i][0][0][1] += dp[i-1][0][0][0]%M
    dp[i][1][0][1] += dp[i-1][1][0][0]%M
    dp[i][0][1][1] += dp[i-1][0][0][1]%M
    dp[i][1][1][1] += (dp[i-1][1][0][1]+dp[i-1][1][0][2])%M
    # 높이 2
    dp[i][1][0][2] += (dp[i-1][0][0][0]+dp[i-1][1][0][0])%M
    dp[i][1][1][2] += (dp[i-1][0][0][1]+dp[i-1][1][0][1])%M
    # 높이 0
    dp[i][0][0][0] += (dp[i-1][0][1][1]+dp[i-1][0][0][1]+dp[i-1][0][0][0])%M
    dp[i][1][0][0] += (dp[i-1][1][1][2]+dp[i-1][1][1][1]+dp[i-1][1][0][2]+dp[i-1][1][0][1]+dp[i-1][1][0][0])%M

print((sum(dp[-1][1][0])+sum(dp[-1][1][1]))%M)